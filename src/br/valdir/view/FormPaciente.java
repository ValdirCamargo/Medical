    public void preencherBairro() {
        conex.conexao();
        conex.executaSql("Select * from bairro order by bai_nome");
        try {
            conex.rs.first();
            jcVila.removeAllItems();
            do {
                jcVila.addItem(conex.rs.getString("bai_nome"));
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher bairros" + ex);
        }
        conex.desconecta();
    }

