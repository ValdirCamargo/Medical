    private void jTableResultadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableResultadoMouseClicked
        String nome_paciente = "" + jTableResultado.getValueAt(jTableResultado.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("SELECT * FROM pacientes where pac_nome='" + nome_paciente + "'");
        try {
            conex.rs.first();
            jtId.setText(String.valueOf(conex.rs.getInt("cod_paciente")));
            jtNome.setText(conex.rs.getString("pac_nome"));
            jfRG.setText(conex.rs.getString("pac_rg"));
            jfTelefone.setText(conex.rs.getString("pac_telefone"));
            jfCel.setText(conex.rs.getString("pac_celular"));
            jfNascimento.setText(conex.rs.getString("pac_nasc"));
            jtRua.setText(conex.rs.getString("pac_rua"));
            jtCep.setText(conex.rs.getString("pac_cep"));
            jtNum.setText(conex.rs.getString("pac_numero"));
            jtComplemento.setText(conex.rs.getString("pac_complemento"));
            jtCidade.setText(conex.rs.getString("pac_cidade"));
            jtEstado.setText(conex.rs.getString("pac_estado"));
            ConexaoBD conexaoBai = new ConexaoBD();
            conexaoBai.conexao();
            conexaoBai.executaSql("select * from bairro where cod_bairro='" + conex.rs.getInt("pac_cod_bairro") + "'");
            conexaoBai.rs.first();
            jcVila.setSelectedItem(conexaoBai.rs.getString("bai_nome"));

            conexaoBai.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao selecionar os dados" + ex);
        }
        conex.desconecta();
        jbtAlterar.setEnabled(true);
        jbtExcluir.setEnabled(true);
        jbtNovo.setEnabled(true);
        jbtCancelar.setEnabled(true);
    }//GEN-LAST:event_jTableResultadoMouseClicked

