    private void jMenuItemRelPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemRelPacienteActionPerformed
        //Esse metodo vai chamar nosso relatório de pacientes
            
        try {
            conecta.executaSql("SELECT * FROM pacientes order by pac_nome");
            JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs);
            JasperPrint jpPrint = JasperFillManager.fillReport("C:\\Users\\Valdir\\Desktop\\Relatorios\\Relatório de Pacientes.jasper", new HashMap(),relatResul);
            JasperViewer jv = new JasperViewer(jpPrint,false);
            jv.setVisible(true);
            jv.toFront();
        } catch (JRException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItemRelPacienteActionPerformed

