    private void jMenuItemCadUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemCadUsuarioActionPerformed
         try {
            conecta.executaSql("SELECT * FROM usuarios WHERE uso_usuario='"+jLabelUsuario.getText()+"'");
            conecta.rs.first();
          
            if (conecta.rs.getString("uso_tipo").equals("administrador")) {
                    if (telaUsu == null) {
                    telaUsu = new FormUsuario();
                    telaUsu.setVisible(true);
                }else{
                    telaUsu.setVisible(true);
                }
            }else{
                JOptionPane.showMessageDialog(null,"Você não tem permissão de Administrador para esse recurso.\n Somente Administradores podem cadastrar novos médicos.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Você não tem permissão de Administrador para esse recurso.\n Somente Administradores podem cadastrar novos médicos..");
        }
        
        
    }//GEN-LAST:event_jMenuItemCadUsuarioActionPerformed

