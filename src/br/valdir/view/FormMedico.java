    private void jTableResultadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableResultadoMouseClicked
        String nome_medico = ""+jTableResultado.getValueAt(jTableResultado.getSelectedRow(),1);
        conex.conexao();
        conex.executaSql("SELECT * FROM medicos where nome='"+nome_medico+"'");
        try {
            conex.rs.first();
            jTextFieldCodMed.setText(String.valueOf(conex.rs.getInt("cod_medicos")));
            jTextFieldPesquisa.setText(conex.rs.getString("nome"));
            jTextFieldNomeMedico.setText(conex.rs.getString("nome"));
            jComboBoxEspecialidade.setSelectedItem(conex.rs.getString("especialidade"));
            jFormattedTextFieldCrm.setText(String.valueOf(conex.rs.getInt("crm")));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao selecionar os dados"+ex);
        }
        conex.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonNovo.setEnabled(true);
    }//GEN-LAST:event_jTableResultadoMouseClicked

