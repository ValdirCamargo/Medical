    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String []colunas = new String[]{"Cod_Medico","Nome","Especialidade","CRM"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
               dados.add(new Object[]{conex.rs.getInt("cod_medicos"),conex.rs.getString("nome"),conex.rs.getString("especialidade"),conex.rs.getInt("crm")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane,"Medico não cadastrado");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableResultado.setModel(modelo);
        jTableResultado.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTableResultado.getColumnModel().getColumn(0).setResizable(false);
        jTableResultado.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTableResultado.getColumnModel().getColumn(1).setResizable(false);
        jTableResultado.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableResultado.getColumnModel().getColumn(2).setResizable(false);
        jTableResultado.getColumnModel().getColumn(3).setPreferredWidth(85);
        jTableResultado.getColumnModel().getColumn(3).setResizable(false);
        jTableResultado.getTableHeader().setReorderingAllowed(false);
        jTableResultado.setAutoResizeMode(jTableResultado.AUTO_RESIZE_OFF);
        jTableResultado.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }
  
    
