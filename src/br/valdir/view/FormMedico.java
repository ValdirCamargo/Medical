    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jTextFieldNomeMedico.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,"O campo NOME nao pode estar vazio");
            jTextFieldNomeMedico.requestFocus();
        }else if(jFormattedTextFieldCrm.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"O campo CRM nao pode estar vazio");
            jFormattedTextFieldCrm.requestFocus();
    }else
        if(flag == 1){    
            mod.setNome(jTextFieldNomeMedico.getText());
            mod.setEspecialidade((String) jComboBoxEspecialidade.getSelectedItem());
            mod.setCrm(Integer.parseInt(jFormattedTextFieldCrm.getText()));
            control.salvar(mod);
            jTextFieldNomeMedico.setText(null);
            jTextFieldNomeMedico.setEnabled(false);
            jComboBoxEspecialidade.setEnabled(false);
            jFormattedTextFieldCrm.setText(null);
            jFormattedTextFieldCrm.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jTextFieldPesquisa.setEnabled(true);
            preencherTabela("select * from medicos order by nome");
        }else{
            mod.setCodigo((Integer.parseInt(jTextFieldCodMed.getText())));
            mod.setNome(jTextFieldNomeMedico.getText());
            mod.setEspecialidade((String) jComboBoxEspecialidade.getSelectedItem());
            mod.setCrm(Integer.parseInt(jFormattedTextFieldCrm.getText()));
            control.Alterar(mod);
            jTextFieldCodMed.setText(null);
            jTextFieldPesquisa.setText(null);
            jTextFieldNomeMedico.setText(null);
            jTextFieldNomeMedico.setEnabled(false);
            jComboBoxEspecialidade.setEnabled(false);
            jFormattedTextFieldCrm.setText(null);
            jFormattedTextFieldCrm.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            preencherTabela("select * from medicos order by nome");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

