    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String []colunas = new String[]{"Cod_Usuário","Nome","Usuário","Tipo"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
               dados.add(new Object[]{conex.rs.getInt("cod_usuario"),conex.rs.getString("uso_nome"),conex.rs.getString("uso_usuario"),conex.rs.getString("uso_tipo")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            //JOptionPane.showMessageDialog(rootPane,"Usuario não cadastrado");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jtTabela.setModel(modelo);
        jtTabela.getColumnModel().getColumn(0).setPreferredWidth(50);
        jtTabela.getColumnModel().getColumn(0).setResizable(false);
        jtTabela.getColumnModel().getColumn(1).setPreferredWidth(300);
        jtTabela.getColumnModel().getColumn(1).setResizable(false);
        jtTabela.getColumnModel().getColumn(2).setPreferredWidth(150);
        jtTabela.getColumnModel().getColumn(2).setResizable(false);
        jtTabela.getColumnModel().getColumn(3).setPreferredWidth(105);
        jtTabela.getColumnModel().getColumn(3).setResizable(false);
        jtTabela.getTableHeader().setReorderingAllowed(false);
        jtTabela.setAutoResizeMode(jtTabela.AUTO_RESIZE_OFF);
        jtTabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }
    
