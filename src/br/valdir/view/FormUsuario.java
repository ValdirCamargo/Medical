    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed
        String senha = new String(jtSenha.getPassword());
        if (jtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo NOME nao pode estar vazio");
            jtNome.requestFocus();
        } else if (jtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo USUÁRIO nao pode estar vazio");
            jtUsuario.requestFocus();
        } else if (jtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo SENHA nao pode estar vazio");
        } else if (jtConfSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo CONFIRMA SENHA nao pode estar vazio");
        } else if(!jtSenha.getText().equals(jtConfSenha.getText())){
            JOptionPane.showMessageDialog(null, "As senhas naop conferem");
            jtSenha.setText("");
            jtConfSenha.setText("");
            jtSenha.requestFocus();
        }else
            
            if (flag == 1) {
            mod.setUsoNome(jtNome.getText());
            mod.setUsoUsuario(jtUsuario.getText());
            mod.setUsoSenha(senha);
            mod.setUsoTipo((String) jcTipo.getSelectedItem());
            dao.salvar(mod);
            jtNome.setText("");
            jtNome.setEnabled(false);
            jtUsuario.setText("");
            jtUsuario.setEnabled(false);
            jtSenha.setText("");
            jtSenha.setEnabled(false);
            jtConfSenha.setText("");
            jtConfSenha.setEnabled(false);
            jbtCancelar.setEnabled(false);
            jbtSalvar.setEnabled(false);
            jbtNovo.setEnabled(true);
            jtPesquisar.setEnabled(true);
            jLabelPesquisar.setVisible(true);
            preencherTabela("select * from usuarios order by uso_nome");
        }else{
            mod.setUsoCod((Integer.parseInt(jtCod.getText())));
            mod.setUsoNome(jtNome.getText());
            mod.setUsoUsuario(jtUsuario.getText());
            mod.setUsoSenha(senha);
            mod.setUsoTipo((String) jcTipo.getSelectedItem());
            dao.Editar(mod);
            jtNome.setText("");
            jtNome.setEnabled(false);
            jtUsuario.setText("");
            jtUsuario.setEnabled(false);
            jtSenha.setText("");
            jtSenha.setEnabled(false);
            jtConfSenha.setText("");
            jtConfSenha.setEnabled(false);
            jbtCancelar.setEnabled(false);
            jbtSalvar.setEnabled(false);
            jbtNovo.setEnabled(true);            
            jtPesquisar.setEnabled(true);
            jLabelPesquisar.setVisible(true);
            preencherTabela("select * from usuarios order by uso_nome");  
            }
    }//GEN-LAST:event_jbtSalvarActionPerformed

